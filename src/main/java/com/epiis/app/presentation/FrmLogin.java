    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        try {
            String email = txtEmail.getText();
            String password = new String(txtPassword.getPassword());
            
            DtoPatient p = this.businessPatient.getByEmailAndPasssword(email, password);

            if (p != null && p.getStatus().equals("activo")) {
                    new FrmClinica(p).setVisible(true);
                    this.dispose();
                    return;
            } if( p != null && p.getStatus().equals("desactivo")) {
                Message.error(this, "Su cuenta se encuentra suspendido... comunicate con soporte");
                return;
            }

            DtoEmployee e = this.businessEmployee.getByEmailAndPassword(email, password);

            if (e == null && p == null) {
                Message.error(this, "Credenciales incorrectas.");
                return;
            }

            if (e != null && e.getStatus().equals("desactivo")) {
                Message.error(this, "Su cuenta se encuentra suspendido... comunicate con soporte");
                return;
            }

            switch (e.getRole()) {
                case "administrador" -> new FrmDashboardAdmin(e).setVisible(true);

                case "medico" -> new FrmDashboardMedico(e).setVisible(true);

                case "recepcionista" -> new FrmDashboardRecep(e).setVisible(true);

                default -> {
                    Message.error(this, "Rol desconcido");
                    return;
                }
            }

            this.dispose();

        } catch (IllegalArgumentException | SQLException ex) {
            Message.error(this, ex.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed


